#!/usr/bin/env python3
import math

def floor(n):
    """
    Renvoie le plancher (floor) du nombre n.

    Args:
        n: Le nombre entier ou flottant dont on veut le plancher.

    Returns:
        int: Le plus grand entier inférieur ou égal à n.
    """
    # Utilise directement math.floor pour obtenir le plancher
    return math.floor(n)
